
namespace Verse.Schemas.Json;

/// <summary>
/// JSON native value content type.
/// </summary>
public enum JsonType
{
    /// <summary>
    /// Undefined (null) value.
    /// </summary>
    Void,

    /// <summary>
    /// Boolean value.
    /// </summary>
    Boolean,

    /// <summary>
    /// Number value.
    /// </summary>
    Number,

    /// <summary>
    /// String value.
    /// </summary>
    String
}